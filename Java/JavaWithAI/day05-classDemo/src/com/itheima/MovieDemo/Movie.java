package com.itheima.MovieDemo;

public class Movie {
    private int id;
    private String name;
    private double score;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public double getPrice() {
        return score;
    }
    public void setPrice(double score) {
        this.score = score;
    }

    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
}
